// Calculadora com operações: somar (+), subtrair (-), multiplicar (*) e dividir (/)

function somar(a, b) {
    return a + b
}

function subtrair(a, b) {
    return a - b
}

function multiplicar(a, b) {
    return a * b
}

function dividir(a, b) {
    return a / b
}

console.log("2 + 4 = " + somar(2, 4))